# A short R script to generate an unordered set of random numbers within
# a specified range, and of a specified size (i.e. how many numbers?)
# e.g to generate a set of unordered random numbers between 10 and 50

seq(10, 50, length.out = 15)    # generates 15 numbers between 10 and 50 that are evenly 
                                # spaced
